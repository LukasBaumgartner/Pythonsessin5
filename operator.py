# Arithmetic operators ( +, -, etc)
print("2+4=", 2+4)
print("6.4-4.2=", 6.4-4.2)
print("9*9", 9*9)

print("64/8=", 64/8)
# modula, % gives the remainder, does not divide, but if divided how much of the values would be left
print("9%2=", 9%2)
# 1
print("99%100", 99%100)
# 99


# integer result for division requires two backslashes
print("8/2 =", 8//2)

# to the power of is **
print("3^4= ", 3**4)

# absolute values
print(abs(-1))

# To print as an integer.
print(int("123"))
